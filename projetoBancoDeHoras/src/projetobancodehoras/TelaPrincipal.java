    public void restaurarDadosComboBoxProjeto(){
        
        try{
            DAO dao = new DAO();
            ResultSet rs = dao.listaProjeto();
            
            while(rs.next()){
                comboProjetos.addItem(rs.getString(1));
            }
        }catch(SQLException erro){
            JOptionPane.showMessageDialog(null,"erro");
        }
    }

